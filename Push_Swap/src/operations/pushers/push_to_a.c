#include "../../../includes/push_swap.h"

void	pa(c_stack **a, c_stack **b)
{
	c_stack	*tmp;
	if (*b)
	{
		tmp = *b;
		*b = (*b) ->next;
		tmp -> next = *a;
		*a = tmp;
		write(1, "pa\n", 3);
	}
	
}
